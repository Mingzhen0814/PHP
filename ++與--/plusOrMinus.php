<?php
    // & |  ^(XOR)  二進位計算
    $c = 3;
    $d = 2;
    echo $c & $d; // 2

    /*
    3         1 1
    2      &  1 0
    --------------  
              1 0
    */

    echo $c | $d; // 3

    /*
    3         1 1
    2      |  1 0
    --------------  
              1 1
    */

    echo $c ^ $d; // 1

    /*
    3         1 1
    2      ^  1 0
    --------------  
              0 1
    */
?>

